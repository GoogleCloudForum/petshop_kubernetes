        public Startup(IConfiguration configuration)
        {
            Configuration = configuration;
        }

        public IConfiguration Configuration { get; }

        // This method gets called by the runtime. Use this method to add services to the container.
        public void ConfigureServices(IServiceCollection services)
        {
            // Add framework services.
            services.AddMvc();

            //Add IOptions<T> for settings
            services.AddOptions();

            services.Configure<ConnectionSettings>(Configuration.GetSection("PetshopSettings"));

            var section = Configuration.GetSection("PetshopSettings");
            if (section.GetValue<string>("CachingEnabled") == "true")
                RedisCacheManager.Initialize(section.GetValue<string>("RedisConnectionString"), section.GetValue<int>("CacheExpiryTime"));

        }

        // This method gets called by the runtime. Use this method to configure the HTTP request pipeline.
        public void Configure(IApplicationBuilder app, IHostingEnvironment env, ILoggerFactory loggerFactory)
        {
            loggerFactory.AddConsole(Configuration.GetSection("Logging"));
            loggerFactory.AddDebug();

            app.UseMvc();
        }
